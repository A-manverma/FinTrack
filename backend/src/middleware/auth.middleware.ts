import { Request, Response, NextFunction } from 'express';
import jwt from 'jsonwebtoken';
import prisma from '../utils/prisma';

export interface AuthRequest extends Request {
    user?: {
        id: string;
        role: string;
    };
}

export const authenticate = (req: AuthRequest, res: Response, next: NextFunction) => {
    const token = req.headers.authorization?.split(' ')[1];

    if (!token) {
        return res.status(401).json({ message: 'Authentication required' });
    }

    try {
        const decoded = jwt.verify(token, process.env.JWT_ACCESS_SECRET!) as { id: string; role: string };
        req.user = decoded;
        next();
    } catch (error) {
        return res.status(401).json({ message: 'Invalid or expired token' });
    }
};

export const authorizeSystemAdmin = (req: AuthRequest, res: Response, next: NextFunction) => {
    if (req.user?.role !== 'SYSTEM_ADMIN') {
        return res.status(403).json({ message: 'Access denied: System Admin only' });
    }
    next();
};

export const checkOwnership = (model: any) => {
    return async (req: AuthRequest, res: Response, next: NextFunction) => {
        const { id } = req.params;
        const userId = req.user?.id;

        if (!userId) return res.status(401).json({ message: 'Unauthorized' });

        try {
            const resource = await (prisma as any)[model].findUnique({
                where: { id },
            });

            if (!resource) {
                return res.status(404).json({ message: 'Resource not found' });
            }

            // Check if the user is the owner (creator)
            if (resource.ownerId !== userId && req.user?.role !== 'SYSTEM_ADMIN') {
                return res.status(403).json({ message: 'Access denied: Ownership required' });
            }

            next();
        } catch (error) {
            res.status(500).json({ message: 'Internal server error' });
        }
    };
};
